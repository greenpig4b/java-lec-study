package ex04;
//모든 메서드는 클래스안에 만들어야한다.
public class Calculator {

    public int add(int n1,int n2){
        return n1 + n2;
    }

    public int minus(int n1,int n2){
        return n1 - n2;
    }

    public int divide(int n1,int n2){
        return n1 / n2;
    }

    public int multi(int n1,int n2){
        return n1 * n2;
    }
}
