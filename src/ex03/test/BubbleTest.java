package ex03.test;

public class BubbleTest {
    public static void main(String[] args) {
        int[] arr = {5,8,2,4,3};

        final int N = arr.length;

        //4바퀴 돌리기
        for (int i = 0; i < N - 1; i++) {
            // System.out.println("1");
            for (int x = 0; x < N - 1; x++) {
                System.out.println("돌아");
                //두번째 조건 4바퀴 도는데 1회전때 4바퀴를 돌려라
            }
            System.out.println();
        }
    }
}
